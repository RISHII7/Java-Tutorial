public class Main {
    public static void main(String[] args) {
		int num1 = 9;      // 32-bit signed integer
        byte by = 127;     // 8-bit signed integer
        short sh = 558;    // 16-bit signed integer
        long l = 5854L;    // 64-bit signed integer

        float f = 5.8f;    // 32-bit floating point
        double d = 5.8;     // 64-bit floating point

        char c = 'k';      // 16-bit Unicode character

        boolean b = true;  // Represents true or false

        // Output the values
        System.out.println("Integer: " + num1);
        System.out.println("Byte: " + by);
        System.out.println("Short: " + sh);
        System.out.println("Long: " + l);
        System.out.println("Float: " + f);
        System.out.println("Double: " + d);
        System.out.println("Char: " + c);
        System.out.println("Boolean: " + b);
	};
};
